int		ft_count_if(char **tab, int (*f)(char*))
{
	int kol;
	int i;

	kol = 0;
	i = 0;
	while (tab[i] != '\0')
	{
		if (f(tab[i]) == 1)
			kol++;
		i++;
	}
	return (kol);
}
